    {
        $arr=input('get.');
        $user=db('user')->where('name',$arr['name'])->find();
        if ($user==null) {
            return json(['code'=>'0','info'=>"账号不存在"]);
        }
        if ($user['pass']!=$arr['pass']) {
            return json(['code'=>'0','info'=>"密码错误"]);
        }else{
            session('user',$user);
            return json(['code'=>'1','info'=>"登录成功"]);
        }
    }

